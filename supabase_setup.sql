ON CONFLICT (phone) DO UPDATE
SET is_admin = 1;

-- 6. OTP policies (allow creation/verification without auth)
DROP POLICY IF EXISTS "Enable insert for all" ON otps;
CREATE POLICY "Enable insert for all" ON otps
FOR INSERT WITH CHECK (true);

DROP POLICY IF EXISTS "Enable read for all" ON otps;
CREATE POLICY "Enable read for all" ON otps
FOR SELECT USING (true);

DROP POLICY IF EXISTS "Enable delete for all" ON otps;
CREATE POLICY "Enable delete for all" ON otps
FOR DELETE USING (true);

-- 7. Sessions Table (for Express backend persistent sessions)
DROP TABLE IF EXISTS sessions;
CREATE TABLE sessions (
  sid TEXT PRIMARY KEY,
  sess JSON NOT NULL,
  expire TIMESTAMP WITH TIME ZONE NOT NULL
);

-- Deny public read on sessions
ALTER TABLE sessions ENABLE ROW LEVEL SECURITY;
DROP POLICY IF EXISTS "Deny Public Sessions" ON sessions;
CREATE POLICY "Deny Public Sessions" ON sessions
FOR ALL TO anon USING (false);

-- 8. Baileys Authentication Table (Persistent WhatsApp Bot)
DROP TABLE IF EXISTS baileys_auth;
CREATE TABLE baileys_auth (
  id TEXT PRIMARY KEY,
  data JSONB NOT NULL
);

-- Deny public read on bot keys
ALTER TABLE baileys_auth ENABLE ROW LEVEL SECURITY;
DROP POLICY IF EXISTS "Deny Public Baileys Auth" ON baileys_auth;
CREATE POLICY "Deny Public Baileys Auth" ON baileys_auth
FOR ALL TO anon USING (false);
